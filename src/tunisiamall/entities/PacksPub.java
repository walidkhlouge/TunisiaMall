package tunisiamall.entities;


public class PacksPub {
int id;
String description;
String nom;
float prix;

    public PacksPub() {
    }

    public PacksPub(int id, String description, String nom, float prix) {
        this.id = id;
        this.description = description;
        this.nom = nom;
        this.prix = prix;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public float getPrix() {
        return prix;
    }

    public void setPrix(float prix) {
        this.prix = prix;
    }

}
